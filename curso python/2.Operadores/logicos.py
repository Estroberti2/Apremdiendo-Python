# Operadores Logicos

#Estos son los distintos tipos de operadores con los que puede trabajar con valores booleanos

# Operador 'and'
# Evalua los valores dados y solo da 'true' cuando ambas o todas son verdaderas

print(True and False) #false
print(True and True) #true


# Operador 'or'
# Evalua si una de la dos se cumple, este da 'false' cuando ninguna se cumplen

print(True and True) #treue
print(False and False) #false


# Operador 'not'
# Devuelve lo contrario al booleano indicado

print(not False) #true
print(not True) #false 