# Operador '=' (igual)
variable = "contenido"
# dicho operador asigna a la variable el contenido


# Operador '+=' (suma)
a = 1
a += 5
# en este cao se crea una variable y se le agrega el valor "1"
# a dicha variable le aumentamos "5"
# esto es lo mismo que hacer lo siguiente (a = a + 5)


# Operador '-=' (resta)
a = 10
a -= 5
# en este cao se crea una variable y se le agrega el valor "10"
# a dicha variable le restamos "5"
# esto es lo mismo que hacer lo siguiente (a = a - 5)


# Operador '/=' (division)
a = 10
a /= 2
# en este cao se crea una variable y se le agrega el valor "10"
# a dicha variable lo repartimos entre "2"
# esto es lo mismo que hacer lo siguiente (a = a / 2)


# Operador '//=' (division entera)
a = 11
a //= 2
# en este cao se crea una variable y se le agrega el valor "10"
# a dicha variable lo repartimos entre "2"
# esto nos dara la division sin punto flotante
# esto es lo mismo que hacer lo siguiente (a = a / 2)


# Operador '*=' (multiplicacion)
a = 5
a *= 5
# en este cao se crea una variable y se le agrega el valor "5"
# a dicha variable lo multiplicamos "5"
# esto es lo mismo que hacer lo siguiente (a = a * 5)


# Operador '**=' (exponente)
a = 5
a **= 3
# en este cao se crea una variable y se le agrega el valor "5"
# a dicha variable lo elevamos a la "3" (al cubo)
# esto es lo mismo que hacer lo siguiente (a = a ** 3)


# Operador '%=' (resto)
a = 5
a %= 2
# en este cao se crea una variable y se le agrega el valor "5"
# a dicha variable lo divide en "2" y nos muestra el resto del calculo (en estecaso el resto es "1")
# esto es lo mismo que hacer lo siguiente (a = a % 2)